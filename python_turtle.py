import turtle as t 
def drawSnake(rad,angle,len,neckrad):
    colors=['235, 66, 53','235, 66, 53','68,133,245','68,133,245','53,168,80','53,168,80']
    for i,color in zip(range(len),colors):
        t.pencolor(eval(color))
        t.circle(rad,angle)
        t.circle(-rad,angle)
    t.circle(rad,angle/2)
    t.fd(rad)
    t.color(235, 66, 53)
    t.circle(neckrad+1,180)
    t.fd(rad*2/3)


def eye(size):
    t.pensize(size)
    t.penup()
    t.forward(-10)
    t.pendown()
    t.pencolor(0,0,0)
    t.fillcolor(0,0,0)
    t.begin_fill()
    t.circle(-size)
    t.end_fill()




